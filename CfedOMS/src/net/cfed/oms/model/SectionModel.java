package net.cfed.oms.model;

/**
 * @author nijesh
 *
 */
public class SectionModel {
	
	private int sectionId = 0;
	private String sectionName = null;
	private String sectionHeadName = null;
	private String sectionHeadEmp = null;
	private String sectionStatus = null;
	private String sectionTasks = null;
	
	/**
	 * @return the sectionId
	 */
	public int getSectionId() {
		return sectionId;
	}
	/**
	 * @param sectionId the sectionId to set
	 */
	public void setSectionId(int sectionId) {
		this.sectionId = sectionId;
	}
	/**
	 * @return the sectionName
	 */
	public String getSectionName() {
		return sectionName;
	}
	/**
	 * @param sectionName the sectionName to set
	 */
	public void setSectionName(String sectionName) {
		this.sectionName = sectionName;
	}
	/**
	 * @return the sectionHeadName
	 */
	public String getSectionHeadName() {
		return sectionHeadName;
	}
	/**
	 * @param sectionHeadName the sectionHeadName to set
	 */
	public void setSectionHeadName(String sectionHeadName) {
		this.sectionHeadName = sectionHeadName;
	}
	/**
	 * @return the sectionHeadEmp
	 */
	public String getSectionHeadEmp() {
		return sectionHeadEmp;
	}
	/**
	 * @param sectionHeadEmp the sectionHeadEmp to set
	 */
	public void setSectionHeadEmp(String sectionHeadEmp) {
		this.sectionHeadEmp = sectionHeadEmp;
	}
	/**
	 * @return the sectionStatus
	 */
	public String getSectionStatus() {
		return sectionStatus;
	}
	/**
	 * @param sectionStatus the sectionStatus to set
	 */
	public void setSectionStatus(String sectionStatus) {
		this.sectionStatus = sectionStatus;
	}
	/**
	 * @return the sectionTasks
	 */
	public String getSectionTasks() {
		return sectionTasks;
	}
	/**
	 * @param sectionTasks the sectionTasks to set
	 */
	public void setSectionTasks(String sectionTasks) {
		this.sectionTasks = sectionTasks;
	}
	
	
	

}
